public class HelloWorld {
    // class block
    public static void main(String[] args) {
        // method block
        /*
         * public / private / protected: 메서드 접근 제어자 (없어도 됨)
         * static: static 여부 정의 (없어도 됨)
         * return type / void : 리턴되는 값의 자료형
         */
        System.out.println("Hello world"); // statement
    }
}